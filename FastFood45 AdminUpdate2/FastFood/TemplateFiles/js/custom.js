// to get current year
function getYear() {
    var currentDate = new Date();
    var currentYear = currentDate.getFullYear();
    document.querySelector("#displayYear").innerHTML = currentYear;
}

getYear();

$(document).ready(function () {
    function getUrlVars() {
        var vars = [], hash;
        var hashes = window.location.href.slice(window.location.href.indexOf('?') + 1).split('&');
        for (var i = 0; i < hashes.length; i++) {
            hash = hashes[i].split('=');
            vars.push(hash[0]);
            vars[hash[0]] = hash[1];
        }
        return vars;
    };

    var id = getUrlVars()["id"];
    if (id > 0) {
        $('.filters_menu li').removeClass('active');
        $('.filters_menu li a[href*="id=' + id + '"]').closest('li').addClass('active'); // Activar la categoría correspondiente

        var data = $('.filters_menu li a[href*="id=' + id + '"]').closest('li').attr('data-filter');
        $(".grid").isotope({ filter: data }); // Filtrar productos
    }
}); $(document).ready(function () {
    $('select').niceSelect();
});

